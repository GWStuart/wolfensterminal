void setup_screen() {
    initscr();
    noecho();  // stop typed characters from showing up
    curs_set(0); // hide cursor
    start_color();

    // initialise colour pairs

    // CONVERT COLOUR FROM LIGHT TO DARK = ADD 6
    init_pair(1, 160, 0); // red
    init_pair(7, 88, 0);

    init_pair(2, 34, 0); // green
    init_pair(8, 47, 0);

    init_pair(3, 21, 0); // blue
    init_pair(9, 18, 0);

    init_pair(4, 130, 0); // brown
    init_pair(10, 94, 0);

    init_pair(5, 130, 0); // brown
    init_pair(11, 94, 0);

    init_pair(6, 226, 0); // yellow
    init_pair(12, 220, 0);

}

void clear_screen() {
    clear();
    refresh();
}

void render_line(int x, int y, int length, chtype character, int colour) {
    attron(COLOR_PAIR(colour));
    for (int i=0; i<length; i++) {
        mvaddch(y + i, x, character);
    }
    attroff(COLOR_PAIR(colour));
}

void render_rect(int x, int y, int length, int width, chtype character) {
    for (int row=0; row<width; row++) {
        mvhline(y + row, x, character, length);
    }
}

